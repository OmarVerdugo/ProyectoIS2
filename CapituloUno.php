En la primera generación de computadoras el uso de tarjetas perforadas predominaba como la forma de intercambiar datos.<br>
 Este tipo de computadoras, que eran la forma primitiva de los ordenadores actuales, se calentaban con facilidad dado a que<br> sus componentes eran válvulas y cilindros magnéticos donde almacenar la información.
A pesar de estos detalles eran<br>
 perfectas para realizar cálculos complejos. Por lo que fue una ventaja que los militares y científicos no dudaron en usar para<br>
 trabajar con ellas.
<h4>Características de la primera generación:</h4>
<ul>
	<li>-Eran maquinas gigantescas, lentas, consumían mucha electricidad y se calentaban mucho.</li>
	<li>-Usaban el sistema binario para mostrar los datos.</li>
	<li>-Introducían datos y programas con tarjetas perforadas.</li>
	<li>-Almacenaban datos con cilindros magnéticos.</li>
	<li>-Procesaban la información con tubos de vació.</li>
	<li>-Eran muy costosas de hacer (10,000 dolares)</li>
</ul>

